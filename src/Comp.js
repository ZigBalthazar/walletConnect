const Comp = () => {
  const providerOptions = {
    walletconnect: {
      package: WalletConnectProvider, // required
      options: {
        infuraId: "{ID}", // required
      },
    },
  };
  const web3Modal = new Web3Modal({
    network: "mainnet", // optional
    cacheProvider: false, // optional
    disableInjectedProvider: false,
    providerOptions, // required
  });

  const WEB3PROVIDER = async () => {
    await web3Modal.clearCachedProvider();
    const instance = await web3Modal.connect();
    const provider = new ethers.providers.Web3Provider(instance);
    const signer = provider.getSigner();
    const cc = new ethers.Contract(
      "0xAe37Cb4D55be90a6D7fd87B4DCd00264b4543DBb",
      ABI,
      signer
    );
    if((await provider.getNetwork()).chainId == 4){
      alert(await cc.remain(0));

    }
    else{
      // console.log((await provider.getNetwork()))
      alert(`Change the network from ${(await provider.getNetwork()).name} to ${chain}`);

    }
  };

  const RPCJSONPROVIDER = async () => {
    await web3Modal.clearCachedProvider();
    const provider = new ethers.providers.JsonRpcProvider(
      "{INFURA_LINK}"
    );
    const cc = new ethers.Contract(
      "0xAe37Cb4D55be90a6D7fd87B4DCd00264b4543DBb",
      ABI,
      provider
    );
    alert(await cc.remain(0));
  };

  return (
    <div>
      <button onClick={WEB3PROVIDER}>Connect to Wallet</button>
      <button onClick={RPCJSONPROVIDER}>Read Contract without Connection</button>
    </div>
  );
};

export default Comp;
